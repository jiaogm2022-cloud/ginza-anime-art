function replaceTextNode(node, language) {
  const source = language === "en" ? textMap : reverseTextMap;
  const value = node.nodeValue;
  const trimmed = value.trim();
  if (!trimmed || !source[trimmed]) return;
  node.nodeValue = value.replace(trimmed, source[trimmed]);
}

function translateText(language) {
  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent || ["SCRIPT", "STYLE", "NOSCRIPT"].includes(parent.tagName)) {
        return NodeFilter.FILTER_REJECT;
      }
      return NodeFilter.FILTER_ACCEPT;
    },
  });

  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach((node) => replaceTextNode(node, language));
}

function translateAttributes(language) {
  const source = language === "en" ? attrMap : Object.fromEntries(Object.entries(attrMap).map(([zh, en]) => [en, zh]));
  document.querySelectorAll("[alt], [aria-label], [title], [placeholder]").forEach((element) => {
    ["alt", "aria-label", "title", "placeholder"].forEach((attr) => {
      const value = element.getAttribute(attr);
      if (value && source[value]) element.setAttribute(attr, source[value]);
    });
  });
}

function setLanguage(language) {
  const normalized = language === "en" ? "en" : "zh";
  const meta = translations[normalized];
  document.documentElement.lang = normalized === "zh" ? "zh-CN" : "en";
  document.title = meta.title;
  document.querySelector('meta[name="description"]')?.setAttribute("content", meta.description);
  translateText(normalized);
  translateAttributes(normalized);
  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === normalized);
  });
  localStorage.setItem(LANG_KEY, normalized);
}

function getInitialLanguage() {
  const params = new URLSearchParams(window.location.search);
  const urlLang = params.get("lang");
  if (urlLang === "en" || urlLang === "zh") return urlLang;
  return localStorage.getItem(LANG_KEY) || "zh";
}

function closeMenu() {
  const toggle = document.getElementById("menuToggle");
  const nav = document.getElementById("mainNav");
  nav?.classList.remove("open");
  toggle?.classList.remove("open");
  toggle?.setAttribute("aria-expanded", "false");
}

function initMenu() {
  const toggle = document.getElementById("menuToggle");
  const nav = document.getElementById("mainNav");
  if (!toggle || !nav) return;

  toggle.addEventListener("click", () => {
    const open = nav.classList.toggle("open");
    toggle.classList.toggle("open", open);
    toggle.setAttribute("aria-expanded", String(open));
  });

  nav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", closeMenu);
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") closeMenu();
  });

  window.addEventListener("resize", () => {
    if (window.innerWidth > 760) closeMenu();
  });
}

